int ProcessResponse (UINT nMss, SOCKET* from, UINT status, void* pArg)
{
    int iResult = 0;

    switch (nMss) {
        case WM_NETWORK_MESSAGE_1:
            iResult = mSERVER.PROCESS_FOR_NETWORK (from, status, pArg);
            break;
        default:
            break;
    }

    return iResult;
}
